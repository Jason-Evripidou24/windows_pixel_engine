/*
*/
void Renderer::drawMaterialTriangles
(
    Backbuffer& backbuffer,
    std::queue<MaterialTriangle*> material_triangles_queue,
    bool draw_filled
)
{
    while(material_triangles_queue.empty() == false)
    {
        MaterialTriangle* material_triangle = material_triangles_queue.front();
        material_triangles_queue.pop();
        
        this->drawMaterialTriangle(backbuffer, material_triangle, draw_filled);

        delete material_triangle;
    }
}
