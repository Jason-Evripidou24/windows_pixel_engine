namespace Math
{
    struct Vec4_i
    {
        //-----------------------------------------------------------------------------------------------------------------//
        int m_data[4];
        //-----------------------------------------------------------------------------------------------------------------//
        
        //-----------------------------------------------------------------------------------------------------------------//
        // Standard constructor.
        //-----------------------------------------------------------------------------------------------------------------//
        Vec4_i()
        {
            m_data[0] = 0;
            m_data[1] = 0;
            m_data[2] = 0;
            m_data[3] = 0;
        }

        Vec4_i(int x, int y, int z, int w)
        {
            m_data[0] = x;
            m_data[1] = y;
            m_data[2] = z;
            m_data[3] = w;
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Copy constructors.
        //-----------------------------------------------------------------------------------------------------------------//
        Vec4_i(const Vec4_i& other)
        {
            for(int i = 0; i < 4; i++)
            {
                m_data[i] = other.m_data[i];
            }
        }

        Vec4_i& operator=(const Vec4_i& other)
        {
            for(int i = 0; i < 4; i++)
            {
                m_data[i] = other.m_data[i];
            }
            return *this;
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Comparison operator overloads.
        //-----------------------------------------------------------------------------------------------------------------//
        bool operator==(const Vec4_i& other) const
        {
            for(int i = 0; i < 4; i++)
            {
                if(m_data[i] != other.m_data[i])
                {
                    return false;
                }
            }
            return true;
        }

        bool operator!=(const Vec4_i& other) const
        {
            for(int i = 0; i < 4; i++)
            {
                if(m_data[i] == other.m_data[i])
                {
                    return false;
                }
            }
            return true;
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Arithmetic operator overloads.
        //-----------------------------------------------------------------------------------------------------------------//
        Vec4_i operator+(const Vec4_i& rhs) const
        {
            return Vec4_i
            (
                m_data[0] + rhs.m_data[0],
                m_data[1] + rhs.m_data[1],
                m_data[2] + rhs.m_data[2],
                m_data[3] + rhs.m_data[3]
            );
        }

        Vec4_i operator-(const Vec4_i& rhs) const
        {
            return Vec4_i
            (
                m_data[0] - rhs.m_data[0],
                m_data[1] - rhs.m_data[1],
                m_data[2] - rhs.m_data[2],
                m_data[3] - rhs.m_data[3]
            );
        }

        Vec4_i operator*(const int scalar) const
        {
            return Vec4_i
            (
                m_data[0] * scalar,
                m_data[1] * scalar,
                m_data[2] * scalar,
                m_data[3] * scalar
            );
        }

        Vec4_i operator/(const int scalar) const
        {
            return Vec4_i
            (
                m_data[0] / scalar,
                m_data[1] / scalar,
                m_data[2] / scalar,
                m_data[3] / scalar
            );
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
