// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
std::vector<Material> Mesh::loadMtlFile(const std::string& file_folder, const std::string& filename)
{
    //---------------------------------------------------------------------------------------------------------------------//
    std::vector<Material> materials;

    std::ifstream file(file_folder + filename);
    if(!file.is_open()) { return materials; }
    //---------------------------------------------------------------------------------------------------------------------//

    Material curr_material = Material();
    curr_material.m_name = std::string("DEFAULT");

    std::string line;
    while(std::getline(file, line))
    {
        std::stringstream ss(line);

        std::string prefix;
        ss >> prefix;

        //-----------------------------------------------------------------------------------------------------------------//
        // Comment.
        //-----------------------------------------------------------------------------------------------------------------//
        if(prefix == "#") { continue; }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // New material.
        //-----------------------------------------------------------------------------------------------------------------//
        if(prefix == "newmtl")
        {
            // Insert the previous material if we have populated it (DEFAULT will be at index 0).
            if(curr_material.m_name.empty() == false)
            {
                materials.push_back(curr_material);
            }

            curr_material = Material();
            curr_material.m_name = parseName(ss);
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Diffuse colour.
        //-----------------------------------------------------------------------------------------------------------------//
        else if(prefix == "Kd")
        {
            curr_material.m_diffuse = parseColor(ss);
        }
        //-----------------------------------------------------------------------------------------------------------------//
    }

    // Insert the last material if we have populated it.
    if(curr_material.m_name.empty() == false)
    {
        materials.push_back(curr_material);
    }

    return materials;
}
