struct Material
{
    std::string m_name;

    //---------------------------------------------------------------------------------------------------------------------//
    // Lighting colours.
    //---------------------------------------------------------------------------------------------------------------------//
    //Math::Vec3_f m_ambient;
    Math::Vec3_f m_diffuse;
    //Math::Vec3_f m_specular;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Specular properties.
    //---------------------------------------------------------------------------------------------------------------------//
    //float m_shininess;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Transparency.
    //---------------------------------------------------------------------------------------------------------------------//
    //float m_alpha; // 1.0 = opaque, 0.0 = invisible
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Textures.
    //---------------------------------------------------------------------------------------------------------------------//
    //std::shared_ptr<Texture> m_diffuse_texture;
    Texture* m_diffuse_texture;

    //std::string m_specular_texture;
    //std::string m_normal_texture;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Constructor.
    //---------------------------------------------------------------------------------------------------------------------//
    Material()
    {
        m_name = "";
        m_diffuse = Math::Vec3_f();
        m_diffuse_texture = nullptr;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Copy constructors.
    //---------------------------------------------------------------------------------------------------------------------//
    Material(const Material& other)
    {
        m_name = other.m_name;
        m_diffuse = other.m_diffuse;
        m_diffuse_texture = other.m_diffuse_texture;
    }

    Material& operator=(const Material& other)
    {
        m_name = other.m_name;
        m_diffuse = other.m_diffuse;
        m_diffuse_texture = other.m_diffuse_texture;
        return *this;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    uint32_t calcMaterialColor(float u, float v) const
    {
        uint32_t output_color;

        uint32_t material_diffuse_color = Math::convertVec3fToColor(m_diffuse);

        if( (m_diffuse_texture != nullptr) && (m_diffuse_texture->m_data != nullptr) )
        {
            uint32_t material_diffuse_texture_color = m_diffuse_texture->getColorAtCoords(u, v);
            output_color = Math::interpolateUint32(material_diffuse_color, material_diffuse_texture_color, 0.5f);
        }
        else
        {
            output_color = material_diffuse_color;
        }

        return output_color;
    }
    //---------------------------------------------------------------------------------------------------------------------//
};
