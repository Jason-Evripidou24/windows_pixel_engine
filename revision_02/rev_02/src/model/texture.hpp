struct Texture
{
    uint32_t* m_data; // A-R-G-B
    int m_width;
    int m_height;

    Texture()
    {
        m_data = nullptr;
        m_width = 0;
        m_height = 0;
    }
    ~Texture()
    {
        if(m_data != nullptr)
        {
            delete m_data;
        }
    }

    static Texture* loadTextureJpgFile(const std::string& file_folder, const std::string& filename);

    uint32_t getColorAtCoords(const float x, const float y);
};
