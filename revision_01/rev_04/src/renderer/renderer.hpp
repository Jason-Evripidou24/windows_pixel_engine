/*
-   Renderer uses coordinates in clip space where -1 <= x,y,depth <= 1. 
*/
struct Renderer
{
    //---------------------------------------------------------------------------------------------------------------------//
    // Functions.
    //---------------------------------------------------------------------------------------------------------------------//
    void drawPixel
    (
        Backbuffer&    backbuffer,
        const float    x,
        const float    y,
        const float    depth,
        const uint32_t color
    );

    void drawLine
    (
        Backbuffer&    backbuffer,
        Math::Vec3_f   pos_0,
        const uint32_t color_0,
        Math::Vec3_f   pos_1,
        const uint32_t color_1
    );

    void drawWireframeTriangle
    (
        Backbuffer&    backbuffer,
        Math::Vec3_f   pos_0,
        const uint32_t color_0,
        Math::Vec3_f   pos_1,
        const uint32_t color_1,
        Math::Vec3_f   pos_2,
        const uint32_t color_2
    );

    void drawModel
    (
        Backbuffer&  backbuffer,
        Model& model,
        Math::Mat4_f proj_view_matrix
    );
    //---------------------------------------------------------------------------------------------------------------------//
};
