/*
-   A 1 * 1 * 1 cube, with center at (0, 0, 0)
*/
Mesh Mesh::cubeMesh(uint32_t color)
{
    Mesh cube_mesh;
    
    int vertex_count    = 8;
    int triangles_count = 12;

    //-----------------------------------------------------------------------------------------------------------------//
    // Vertices: Position and colour.
    //-----------------------------------------------------------------------------------------------------------------//
    cube_mesh.m_vertices = std::vector<Vertex>(vertex_count);

    cube_mesh.m_vertices[0].m_position = Math::Vec4_f(-0.5f, 0.5f, 0.5f, 1.0f);
    cube_mesh.m_vertices[0].m_color = color;

    cube_mesh.m_vertices[1].m_position = Math::Vec4_f(-0.5f, -0.5f, 0.5f, 1.0f);
    cube_mesh.m_vertices[1].m_color = color;

    cube_mesh.m_vertices[2].m_position = Math::Vec4_f(0.5f, -0.5f, 0.5f, 1.0f);
    cube_mesh.m_vertices[2].m_color = color;

    cube_mesh.m_vertices[3].m_position = Math::Vec4_f(0.5f, 0.5f, 0.5f, 1.0f);
    cube_mesh.m_vertices[3].m_color = color;

    cube_mesh.m_vertices[4].m_position = Math::Vec4_f(-0.5f, 0.5f, -0.5f, 1.0f);
    cube_mesh.m_vertices[4].m_color = color;

    cube_mesh.m_vertices[5].m_position = Math::Vec4_f(-0.5f, -0.5f, -0.5f, 1.0f);
    cube_mesh.m_vertices[5].m_color = color;

    cube_mesh.m_vertices[6].m_position = Math::Vec4_f(0.5f, -0.5f, -0.5f, 1.0f);
    cube_mesh.m_vertices[6].m_color = color;

    cube_mesh.m_vertices[7].m_position = Math::Vec4_f(0.5f, 0.5f, -0.5f, 1.0f);
    cube_mesh.m_vertices[7].m_color = color;
    //-----------------------------------------------------------------------------------------------------------------//

    //-----------------------------------------------------------------------------------------------------------------//
    // Indicies: Each triplet represents a triangle face.
    //-----------------------------------------------------------------------------------------------------------------//
    cube_mesh.m_triangles = std::vector<Math::Vec3_i>(triangles_count);

    // Front Face.
    cube_mesh.m_triangles[0] = Math::Vec3_i(0, 1, 2);
    cube_mesh.m_triangles[1] = Math::Vec3_i(0, 2, 3);
    // Rear Face.
    cube_mesh.m_triangles[2] = Math::Vec3_i(7, 6, 5);
    cube_mesh.m_triangles[3] = Math::Vec3_i(7, 5, 4);
    // Top Face.
    cube_mesh.m_triangles[4] = Math::Vec3_i(4, 0, 3);
    cube_mesh.m_triangles[5] = Math::Vec3_i(4, 3, 7);
    // Bottom Face.
    cube_mesh.m_triangles[6] = Math::Vec3_i(1, 5, 6);
    cube_mesh.m_triangles[7] = Math::Vec3_i(1, 6, 2);
    // Left Face.
    cube_mesh.m_triangles[8] = Math::Vec3_i(4, 5, 1);
    cube_mesh.m_triangles[9] = Math::Vec3_i(4, 1, 0);
    // Right Face.
    cube_mesh.m_triangles[10] = Math::Vec3_i(3, 2, 6);
    cube_mesh.m_triangles[11] = Math::Vec3_i(3, 6, 7);
    //-----------------------------------------------------------------------------------------------------------------//

    return cube_mesh;
}
