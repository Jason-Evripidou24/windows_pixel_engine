void Scene::drawScene(Backbuffer& backbuffer, Renderer& renderer, Camera& camera)
{
    Math::Mat4_f projection_mat = camera.calcProjectionMatrix((float)backbuffer.m_width / (float)backbuffer.m_height);
    Math::Mat4_f view_mat = camera.calcViewMatrix();
    Math::Mat4_f projection_view_mat = projection_mat * view_mat;

    for(Model model : m_models)
    {
        renderer.drawModel(backbuffer, model, projection_view_mat);
    }
}
