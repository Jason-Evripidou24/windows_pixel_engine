/*
-   ax + by + c = 0
*/
namespace Math
{
    struct Line2d
    {
        //-----------------------------------------------------------------------------------------------------------------//
        float m_a;
        float m_b;
        float m_c;

        bool m_is_valid_line;
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        Line2d(const float x_0, const float y_0, const float x_1, const float y_1)
        {
            float dx = x_1 - x_0;
            float dy = y_1 - y_0;

            //-------------------------------------------------------------------------------------------------------------//
            // Case 1: Line is zero length. (0.0f)x + (0.0f)y + 0.0f = 0.0f
            //-------------------------------------------------------------------------------------------------------------//
            if( (Utils::checkFloatEquals(dx, 0.0f) == true) && (Utils::checkFloatEquals(dy, 0.0f) == true) )
            {
                m_a = 0.0f;
                m_b = 0.0f;
                m_c = 0.0f;

                m_is_valid_line = false;

                return;
            }
            //-------------------------------------------------------------------------------------------------------------//

            //-------------------------------------------------------------------------------------------------------------//
            // Case 2: Line is vertical. (1.0f)x + (0.0f)y + c = 0
            //-------------------------------------------------------------------------------------------------------------//
            if(Utils::checkFloatEquals(dx, 0.0f) == true)
            {
                m_a = 1.0f;
                m_b = 0.0f;
                m_c = (-1.0f) * x_0;

                m_is_valid_line = true;

                return;
            }
            //-------------------------------------------------------------------------------------------------------------//

            //-------------------------------------------------------------------------------------------------------------//
            // Case 3: Line is horizontal. (0.0f)x + (1.0f)y + c = 0
            //-------------------------------------------------------------------------------------------------------------//
            if(Utils::checkFloatEquals(dy, 0.0f) == true)
            {
                m_a = 0.0f;
                m_b = 1.0f;
                m_c = (-1.0f) * y_0;

                m_is_valid_line = true;

                return;
            }
            //-------------------------------------------------------------------------------------------------------------//

            //-------------------------------------------------------------------------------------------------------------//
            // Case 4: Line is on a gradient. ax + by + c = 0                        y = ((dy / dx) * x) + c
            //-------------------------------------------------------------------------------------------------------------//
            m_a = dy;
            m_b = (-1.0f) * dx;
            m_c = (dx * y_0) - (dy * x_0);

            m_is_valid_line = true;
            //-------------------------------------------------------------------------------------------------------------//
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        bool checkLineIntersectsXaxis() const
        {
            if( (m_is_valid_line == false) || (Utils::checkFloatEquals(m_a, 0.0f) == true) ) { return false; }
            return true;
        }

        float getIntersectionXaxis() const
        {
            if(checkLineIntersectsXaxis() == true)
            {
                return ((-1.0f) * m_c) / m_a;
            }
            return 0.0f;
        }

        bool checkLineIntersectsYaxis() const
        {
            if( (m_is_valid_line == false) || (Utils::checkFloatEquals(m_b, 0.0f) == true) ) { return false; }
            return true;
        }

        float getIntersectionYaxis() const
        {
            if(checkLineIntersectsYaxis() == true)
            {
                return ((-1.0f) * m_c) / m_b;
            }
            return 0.0f;
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
