namespace Math
{
    struct Line3d
    {
        //-----------------------------------------------------------------------------------------------------------------//
        Vec3_f m_start;
        Vec3_f m_end;
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        bool checkAnyPartOfLineWithinBounds(float x_min, float x_max, float y_min, float y_max, float z_min, float z_max) const
        {
            const float min_bounds[3] = { x_min, y_min, z_min };
            const float max_bounds[3] = { x_max, y_max, z_max };

            float tmin = 0.0f;
            float tmax = 1.0f;

            for(int i = 0; i < 3; i++)
            {
                float d = m_end.m_data[i] - m_start.m_data[i];

                if(Utils::checkFloatEquals(d, 0.0f) == true)
                {
                    if( (m_start.m_data[i] < min_bounds[i]) || (m_start.m_data[i] > max_bounds[i]) )
                    {
                        return false;
                    }
                }
                else
                {
                    float inverse_d = 1.0f / d;
                    float t1 = (min_bounds[i] - m_start.m_data[i]) * inverse_d;
                    float t2 = (max_bounds[i] - m_start.m_data[i]) * inverse_d;

                    if(t1 > t2)
                    {
                        std::swap(t1, t2);
                    }

                    if(t1 > tmin)
                    {
                        tmin = t1;
                    }
                    if(t2 < tmax)
                    {
                        tmax = t2;
                    }

                    if(tmin > tmax)
                    {
                        return false;
                    }
                }
            }

            return true;
        }

        void cutToBounds(float x_min, float x_max, float y_min, float y_max, float z_min, float z_max)
        {
            const float min_bounds[3] = { x_min, y_min, z_min };
            const float max_bounds[3] = { x_max, y_max, z_max };

            float tmin = 0.0f;
            float tmax = 1.0f;

            for(int i = 0; i < 3; i++)
            {
                float d = m_end.m_data[i] - m_start.m_data[i];

                if(Utils::checkFloatEquals(d, 0.0f) == true)
                {
                    if( (m_start.m_data[i] < min_bounds[i]) || (m_start.m_data[i] > max_bounds[i]) )
                    {
                        return;
                    }
                }
                else
                {
                    float inverse_d = 1.0f / d;
                    float t1 = (min_bounds[i] - m_start.m_data[i]) * inverse_d;
                    float t2 = (max_bounds[i] - m_start.m_data[i]) * inverse_d;

                    if(t1 > t2)
                    {
                        std::swap(t1, t2);
                    }

                    if(t1 > tmin)
                    {
                        tmin = t1;
                    }
                    if(t2 < tmax)
                    {
                        tmax = t2;
                    }

                    if(tmin > tmax)
                    {
                        return;
                    }
                }
            }

            Vec3_f original_start = m_start;
            Vec3_f original_end = m_end;

            Vec3_f direction = original_end - original_start;

            m_start = original_start + (direction * tmin);
            m_end   = original_start + (direction * tmax);
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
