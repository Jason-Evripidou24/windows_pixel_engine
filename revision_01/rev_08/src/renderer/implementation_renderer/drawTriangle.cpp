/*
-   Triangle has been transformed to clip space and undergone perspective divide but no clipping against x,y,z planes.
*/
void Renderer::drawTriangle(Backbuffer& backbuffer, const Math::Triangle& triangle, const Material& material, const bool draw_filled)
{
    const std::vector<Math::Triangle> triangles_clipped = Math::clipTriangleBetweenXYZ
    (
        triangle,
        -1.0f,
        1.0f,
        -1.0f,
        1.0f,
        -1.0f,
        1.0f
    );

    for(const Math::Triangle& triangle_clipped : triangles_clipped)
    {
        if(draw_filled == true)
        {
            this->fillTriangle(backbuffer, triangle_clipped, material);
        }
        else
        {
            this->drawLine(backbuffer, triangle_clipped.m_vertices[0], triangle_clipped.m_vertices[1], material);
            this->drawLine(backbuffer, triangle_clipped.m_vertices[0], triangle_clipped.m_vertices[2], material);
            this->drawLine(backbuffer, triangle_clipped.m_vertices[1], triangle_clipped.m_vertices[2], material);
        }
    }
}
