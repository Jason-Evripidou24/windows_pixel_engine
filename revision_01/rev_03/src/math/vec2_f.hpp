namespace Math
{
    struct Vec2_f
    {
        //-----------------------------------------------------------------------------------------------------------------//
        float m_data[2];
        //-----------------------------------------------------------------------------------------------------------------//
        
        //-----------------------------------------------------------------------------------------------------------------//
        Vec2_f() { m_data[0] = 0.0f; m_data[1] = 0.0f; }
        Vec2_f(float x, float y) { m_data[0] = x; m_data[1] = y; }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Operator overloads.
        //-----------------------------------------------------------------------------------------------------------------//
        bool operator==(const Vec2_f& other) const
        {
            for(int i = 0; i < 2; i++)
            {
                if(Utils::checkFloatEquals(m_data[i], other.m_data[i]) == false)
                {
                    return false;
                }
            }
            return true;
        }

        bool operator!=(const Vec2_f& other) const
        {
            for(int i = 0; i < 2; i++)
            {
                if(Utils::checkFloatEquals(m_data[i], other.m_data[i]) == true)
                {
                    return false;
                }
            }
            return true;
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
