// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Math::Vec2_f Math::normalise(const Vec2_f& vec)
{
    Vec2_f new_vec;

    float length = sqrtf
    (
        (vec.m_data[0] * vec.m_data[0]) +
        (vec.m_data[1] * vec.m_data[1])
    );

    if(length == 0.0f)
    {
        new_vec.m_data[0] = 0.0f;
        new_vec.m_data[1] = 0.0f;
        return new_vec;
    }

    new_vec.m_data[0] = vec.m_data[0] / length;
    new_vec.m_data[1] = vec.m_data[1] / length;

    return new_vec;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Math::Vec3_f Math::normalise(const Vec3_f& vec)
{
    Vec3_f new_vec;

    float length = sqrtf
    (
        (vec.m_data[0] * vec.m_data[0]) +
        (vec.m_data[1] * vec.m_data[1]) +
        (vec.m_data[2] * vec.m_data[2])
    );

    if(length == 0.0f)
    {
        new_vec.m_data[0] = 0.0f;
        new_vec.m_data[1] = 0.0f;
        new_vec.m_data[2] = 0.0f;
        return new_vec;
    }

    new_vec.m_data[0] = vec.m_data[0] / length;
    new_vec.m_data[1] = vec.m_data[1] / length;
    new_vec.m_data[2] = vec.m_data[2] / length;
    
    return new_vec;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Math::Vec4_f Math::normalise(const Vec4_f& vec)
{
    Vec4_f new_vec;

    float length = sqrtf
    (
        (vec.m_data[0] * vec.m_data[0]) +
        (vec.m_data[1] * vec.m_data[1]) +
        (vec.m_data[2] * vec.m_data[2]) +
        (vec.m_data[3] * vec.m_data[3])
    );

    if(length == 0.0f)
    {
        new_vec.m_data[0] = 0.0f;
        new_vec.m_data[1] = 0.0f;
        new_vec.m_data[2] = 0.0f;
        new_vec.m_data[3] = 0.0f;
        return new_vec;
    }

    new_vec.m_data[0] = vec.m_data[0] / length;
    new_vec.m_data[1] = vec.m_data[1] / length;
    new_vec.m_data[2] = vec.m_data[2] / length;
    new_vec.m_data[3] = vec.m_data[3] / length;
    
    return new_vec;
}
