namespace Math
{
    struct Mat4_f
    {
        //-----------------------------------------------------------------------------------------------------------------//
        float m_data[16];
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Operator overloads.
        //-----------------------------------------------------------------------------------------------------------------//
        bool operator==(const Mat4_f& other) const
        {
            for(int i = 0; i < 16; i++)
            {
                if(Utils::checkFloatEquals(m_data[i], other.m_data[i]) == false)
                {
                    return false;
                }
            }
            return true;
        }

        bool operator!=(const Mat4_f& other) const
        {
            for(int i = 0; i < 16; i++)
            {
                if(Utils::checkFloatEquals(m_data[i], other.m_data[i]) == true)
                {
                    return false;
                }
            }
            return true;
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
