// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
struct Pixel
{
    //---------------------------------------------------------------------------------------------------------------------//
    // Data.
    //---------------------------------------------------------------------------------------------------------------------//
    uint32_t m_data; // a pixel is BGRA format so (uint8)B(uint8)G(uint8)R(uint8)A
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Functions.
    //---------------------------------------------------------------------------------------------------------------------//
    Pixel() : m_data(0) {}
    Pixel(uint8_t b, uint8_t g, uint8_t r, uint8_t a)
    {
        m_data = 
        (
            (uint32_t(b) << 0)  |
            (uint32_t(g) << 8)  |
            (uint32_t(r) << 16) |
            (uint32_t(a) << 24)
        );
    }

    uint8_t getB() const { return (m_data >> 0)  & 0xFF; }
    uint8_t getG() const { return (m_data >> 8)  & 0xFF; }
    uint8_t getR() const { return (m_data >> 16) & 0xFF; }
    uint8_t getA() const { return (m_data >> 24) & 0xFF; }
    //---------------------------------------------------------------------------------------------------------------------//
};
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
#endif
