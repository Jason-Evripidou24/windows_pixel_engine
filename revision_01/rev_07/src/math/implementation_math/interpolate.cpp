// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
/*
-   t must be within 0.0f <= t <= 1.0f
*/
Math::Vec2_f Math::interpolateVec2f(const Vec2_f& start, const Vec2_f& end, float t)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if(t <= 0.0f) { t = 0.0f; }
    if(t >= 1.0f) { t = 1.0f; }
    //---------------------------------------------------------------------------------------------------------------------//
    
    Math::Vec2_f result;
    for(int i = 0; i < 2; i++)
    {
        result.m_data[i] = Math::interpolateFloat(start.m_data[i], end.m_data[i], t);
    }
    return result;
}
