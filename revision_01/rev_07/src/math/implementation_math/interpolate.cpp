/*
-   t must be within 0.0f <= t <= 1.0f
*/
uint32_t Math::interpolateUint32(const uint32_t start, const uint32_t end, float t)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if(t <= 0.0f) { t = 0.0f; }
    if(t >= 1.0f) { t = 1.0f; }
    //---------------------------------------------------------------------------------------------------------------------//
    
    float section_00 = static_cast<float>((start >> 0) & 0xFF);
    float section_01 = static_cast<float>((start >> 8) & 0xFF);
    float section_02 = static_cast<float>((start >> 16) & 0xFF);
    float section_03 = static_cast<float>((start >> 24) & 0xFF);

    float section_10 = static_cast<float>((end >> 0) & 0xFF);
    float section_11 = static_cast<float>((end >> 8) & 0xFF);
    float section_12 = static_cast<float>((end >> 16) & 0xFF);
    float section_13 = static_cast<float>((end >> 24) & 0xFF);

    uint8_t interpolated_0 = static_cast<uint8_t>( (section_00 * (1.0f - t)) + (section_10 * t) );
    uint8_t interpolated_1 = static_cast<uint8_t>( (section_01 * (1.0f - t)) + (section_11 * t) );
    uint8_t interpolated_2 = static_cast<uint8_t>( (section_02 * (1.0f - t)) + (section_12 * t) );
    uint8_t interpolated_3 = static_cast<uint8_t>( (section_03 * (1.0f - t)) + (section_13 * t) );

    uint32_t result =
        (static_cast<uint32_t>(interpolated_0) << 0)  |
        (static_cast<uint32_t>(interpolated_1) << 8)  |
        (static_cast<uint32_t>(interpolated_2) << 16) |
        (static_cast<uint32_t>(interpolated_3) << 24);
    
    return result;
}
