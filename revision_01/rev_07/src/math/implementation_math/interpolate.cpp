// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
/*
-   t must be within 0.0f <= t <= 1.0f
*/
float Math::interpolateFloat(const float start, const float end, float t)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if(t <= 0.0f) { t = 0.0f; }
    if(t >= 1.0f) { t = 1.0f; }
    //---------------------------------------------------------------------------------------------------------------------//
    
    return (start * (1.0f - t)) + (end * t);
}
