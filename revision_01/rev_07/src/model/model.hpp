// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
struct Model
{
    //---------------------------------------------------------------------------------------------------------------------//
    Mesh* m_mesh;

    Math::Vec3_f m_position;
    Math::Vec3_f m_scale;
    float m_rotate_rad;
    Math::Vec3_f m_rotate_axis;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    Model()
    {
        m_mesh = nullptr;
        m_position = Math::Vec3_f();
        m_scale = Math::Vec3_f();
        m_rotate_rad = 0.0f;
        m_rotate_axis = Math::Vec3_f();
    }
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f calcModelMatrix() const
    {
        Math::Mat4_f translate = Math::translationMat4_f
        (
            m_position.m_data[0],
            m_position.m_data[1],
            m_position.m_data[2]
        );

        Math::Mat4_f scale = Math::scaleMat4_f
        (
            m_scale.m_data[0],
            m_scale.m_data[1],
            m_scale.m_data[2]
        );

        Math::Mat4_f rotate = Math::rotationMat4_f
        (
            m_rotate_axis.m_data[0],
            m_rotate_axis.m_data[1],
            m_rotate_axis.m_data[2],
            m_rotate_rad
        );
        
        Math::Mat4_f model = translate * rotate * scale;

        return model;
    }
    //---------------------------------------------------------------------------------------------------------------------//
};
