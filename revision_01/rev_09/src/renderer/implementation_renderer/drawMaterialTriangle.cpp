/*
-   Triangle has been transformed to clip space and undergone perspective divide but no clipping against x,y,z planes.
*/
void Renderer::drawMaterialTriangle
(
    Backbuffer& backbuffer,
    MaterialTriangle* material_triangle,
    bool draw_filled
)
{
    const std::vector<Math::Triangle> triangles_clipped = Math::clipTriangleBetweenXYZ
    (
        material_triangle->m_triangle,
        -1.0f,
        1.0f,
        -1.0f,
        1.0f,
        -1.0f,
        1.0f
    );

    for(const Math::Triangle& triangle_clipped : triangles_clipped)
    {
        if(draw_filled == true)
        {
            this->fillTriangle(backbuffer, triangle_clipped, material_triangle->m_material);
        }
        else
        {
            this->drawLine(backbuffer, triangle_clipped.m_v0, triangle_clipped.m_v1, material_triangle->m_material);
            this->drawLine(backbuffer, triangle_clipped.m_v0, triangle_clipped.m_v2, material_triangle->m_material);
            this->drawLine(backbuffer, triangle_clipped.m_v1, triangle_clipped.m_v2, material_triangle->m_material);
        }
    }
}
