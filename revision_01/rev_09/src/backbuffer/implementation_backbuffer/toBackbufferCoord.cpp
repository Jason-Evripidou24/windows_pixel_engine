/*
-   x is [-1.0f, 1.0f].
*/
int Backbuffer::toBackbufferCoordX(const float x)
{
    return static_cast<int>((x + 1.0f) * static_cast<float>(m_width) * 0.5f);
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
/*
-   y is [-1.0f, 1.0f].
*/
int Backbuffer::toBackbufferCoordY(const float y)
{
    return static_cast<int>((1.0f - y) * static_cast<float>(m_height) * 0.5f);
}
