bool Window::processMessages()
{
    MSG msg;

    while(PeekMessage(&msg, 0, 0, 0, PM_REMOVE))
    {
        if(msg.message == WM_QUIT)
        {
            m_running = false;
            break;
        }

        TranslateMessage(&msg);
        DispatchMessage(&msg);
    }

    return m_running;
}
