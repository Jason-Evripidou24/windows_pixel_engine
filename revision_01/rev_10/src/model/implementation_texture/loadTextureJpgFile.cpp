Texture* Texture::loadTextureJpgFile(const std::string& file_folder, const std::string& filename)
{
    int width;
    int height;
    int channels;

    std::string file_path_and_name = file_folder + filename;
    unsigned char* pixels = stbi_load(file_path_and_name.c_str(), &width, &height, &channels, 4);
    if(!pixels) { return nullptr; }

    Texture* new_texture = new Texture();

    new_texture->m_width = width;
    new_texture->m_height = height;
    new_texture->m_data = new uint32_t[width * height];

    for(int i = 0; i < (width * height); i++)
    {
        uint8_t r = pixels[(i * 4) + 0];
        uint8_t g = pixels[(i * 4) + 1];
        uint8_t b = pixels[(i * 4) + 2];
        uint8_t a = pixels[(i * 4) + 3];

        new_texture->m_data[i] =
            (a << 24) |
            (r << 16) |
            (g << 8)  |
            (b);
    }

    stbi_image_free(pixels);

    return new_texture;
}
