// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
struct Camera
{
    //---------------------------------------------------------------------------------------------------------------------//
    // Transform.
    //---------------------------------------------------------------------------------------------------------------------//
    Vec3_f m_position;

    // Euler angles (radians).
    float m_pitch_rads; // Rotation around local X axis.
    float m_yaw_rads;   // Rotation around world/local Y axis.
    float m_roll_rads;  // Rotation around local Z axis.
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Projection parameters.
    //---------------------------------------------------------------------------------------------------------------------//
    float m_fov_rads;
    float m_near_plane;
    float m_far_plane;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Orientation vectors.
    //---------------------------------------------------------------------------------------------------------------------//
    Vec3_f m_world_up;

    Vec3_f m_front; // Forward direction.
    Vec3_f m_right; // Right direction.
    Vec3_f m_up;    // Camera up direction.
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    Camera
    (
        const Vec3_f& position,
        const float pitch_rads,
        const float yaw_rads,
        const float roll_rads,
        const float fov_rads,
        const float near_plane,
        const float far_plane,
        const Vec3_f& world_up
    );

    void updateVectors();

    void moveForward(const float offset);
    void moveRight(const float offset);
    void moveUp(const float offset);

    void lookRight(const float offset);
    void lookUp(const float offset);
    void rollRight(const float offset);

    Mat4_f calcModelMatrix() const;
    Mat4_f calcViewMatrix() const;
    Mat4_f calcProjectionMatrix(const float aspect_ratio) const;
    //---------------------------------------------------------------------------------------------------------------------//
};
