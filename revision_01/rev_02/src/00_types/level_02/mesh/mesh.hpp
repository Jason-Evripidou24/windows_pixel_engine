// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
struct Mesh
{
    //---------------------------------------------------------------------------------------------------------------------//
    std::vector<Vertex4_f> m_vertices;
    int m_vertex_count;

    std::vector<int> m_indices;
    int m_index_count;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Basic models.
    //---------------------------------------------------------------------------------------------------------------------//
    static Mesh createCubeMesh()
    {
        Mesh cube_mesh;
        
        cube_mesh.m_vertex_count = 8;
        cube_mesh.m_index_count = 36;

        //-----------------------------------------------------------------------------------------------------------------//
        // Vertices: Position and colour.
        //-----------------------------------------------------------------------------------------------------------------//
        cube_mesh.m_vertices = std::vector<Vertex4_f>(cube_mesh.m_vertex_count);

        cube_mesh.m_vertices[0].m_position = {-0.5f,-0.5f,-0.5f, 1.0f};
        cube_mesh.m_vertices[0].m_color = Pixel(255, 0, 255, 255);

        cube_mesh.m_vertices[1].m_position = { 0.5f,-0.5f,-0.5f, 1.0f};
        cube_mesh.m_vertices[1].m_color = Pixel(255, 0, 255, 255);

        cube_mesh.m_vertices[2].m_position = { 0.5f, 0.5f,-0.5f, 1.0f};
        cube_mesh.m_vertices[2].m_color = Pixel(255, 255, 0, 255);

        cube_mesh.m_vertices[3].m_position = {-0.5f, 0.5f,-0.5f, 1.0f};
        cube_mesh.m_vertices[3].m_color = Pixel(255, 255, 0, 255);

        cube_mesh.m_vertices[4].m_position = {-0.5f,-0.5f, 0.5f, 1.0f};
        cube_mesh.m_vertices[4].m_color = Pixel(255, 0, 255, 255);

        cube_mesh.m_vertices[5].m_position = { 0.5f,-0.5f, 0.5f, 1.0f};
        cube_mesh.m_vertices[5].m_color = Pixel(255, 0, 255, 255);

        cube_mesh.m_vertices[6].m_position = { 0.5f, 0.5f, 0.5f, 1.0f};
        cube_mesh.m_vertices[6].m_color = Pixel(255, 255, 0, 255);

        cube_mesh.m_vertices[7].m_position = {-0.5f, 0.5f, 0.5f, 1.0f};
        cube_mesh.m_vertices[7].m_color = Pixel(255, 255, 0, 255);
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Indicies: Each triplet represents a triangle face.
        //-----------------------------------------------------------------------------------------------------------------//
        cube_mesh.m_indices = std::vector<int>(cube_mesh.m_index_count);

        int idx[] =
        {
            0, 1, 2,  2, 3, 0,
            4, 5, 6,  6, 7, 4,
            0, 3, 7,  7, 4, 0,
            1, 5, 6,  6, 2, 1,
            0, 1, 5,  5, 4, 0,
            3, 2, 6,  6, 7, 3
        };
        for(int i = 0; i < cube_mesh.m_index_count; i++)
        {
            cube_mesh.m_indices[i] = idx[i];
        }
        //-----------------------------------------------------------------------------------------------------------------//

        return cube_mesh;
    }
    //---------------------------------------------------------------------------------------------------------------------//
};
