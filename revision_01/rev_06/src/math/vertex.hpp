namespace Math
{
    struct Vertex
    {
        //-----------------------------------------------------------------------------------------------------------------//
        Vec4_f m_position;
        Vec3_f m_normal;
        Vec2_f m_tex_coords;
        uint32_t m_color;
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        Vertex()
        {
            m_position   = Vec4_f();
            m_normal     = Vec3_f();
            m_tex_coords = Vec2_f();
            m_color      = 0xFF000000; // Black.
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Visulisation.
        //-----------------------------------------------------------------------------------------------------------------//
        std::string toString(int min_num_width, int num_decimals) const
        {
            return
            (
                std::string("VERTEX INFO:") + std::string("\n") +
                m_position.toStringRow(min_num_width, num_decimals) + std::string("\n") +
                m_normal.toStringRow(min_num_width, num_decimals) + std::string("\n") +
                m_tex_coords.toStringRow(min_num_width, num_decimals) + std::string("\n") +
                std::to_string(m_color)
            );
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
