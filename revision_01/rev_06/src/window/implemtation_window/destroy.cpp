void Window::destroy()
{
    m_width  = 0;
    m_height = 0;

    if(m_dc != nullptr)
    {
        ReleaseDC(m_hwnd, m_dc);
        m_dc = nullptr;
    }

    if(m_hwnd != nullptr)
    {
        DestroyWindow(m_hwnd);
        m_hwnd = nullptr;
    }

    m_running = false;
}
