struct ObjFile
{
    struct ObjVertexInfo
    {
        int m_position_and_color = -1;
        int m_tex_coord          = -1;
        int m_normal             = -1;
    };

    struct ObjTriangleInfo
    {
        ObjVertexInfo m_v0;
        ObjVertexInfo m_v1;
        ObjVertexInfo m_v2;
    };

    struct ObjMaterialTriangleInfo
    {
        ObjTriangleInfo m_triangle;
        std::string m_material_name;
    };

    std::vector<std::pair<Math::Vec4_f, Math::Vec4_f>> m_vertex_positions_and_colors;
    std::vector<Math::Vec2_f> m_vertex_tex_coords;
    std::vector<Math::Vec3_f> m_vertex_normals;

    std::vector<ObjMaterialTriangleInfo> m_material_triangles;

    void loadObjFile(const std::string& file_folder, const std::string& filename);
};

struct MtlFile
{
    std::vector<Material> m_materials;
    std::unordered_map<std::string, int> m_material_name_to_index;

    std::vector<Texture*> m_diffuse_textures;
    std::unordered_map<std::string, int> m_diffuse_texture_name_to_index;

    void loadMtlFile(const std::string& file_folder, const std::string& filename);
};

struct Mesh
{
    //---------------------------------------------------------------------------------------------------------------------//
    ObjFile m_obj_file_info;
    MtlFile m_mtl_file_info;

    std::vector<MaterialTriangle> m_material_triangles;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    void loadMeshObjFile(const std::string& file_folder, const std::string& filename);
    //---------------------------------------------------------------------------------------------------------------------//
};
