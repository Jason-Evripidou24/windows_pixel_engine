// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void MtlFile::loadMtlFile(const std::string& file_folder, const std::string& filename)
{
    //---------------------------------------------------------------------------------------------------------------------//
    m_materials.clear();
    m_material_name_to_index.clear();

    m_diffuse_textures.clear();
    m_diffuse_texture_name_to_index.clear();

    std::ifstream file(file_folder + filename);
    if(!file.is_open()) { return; }
    //---------------------------------------------------------------------------------------------------------------------//

    Material curr_material = Material();
    curr_material.m_name = std::string("DEFAULT");

    std::string line;
    while(std::getline(file, line))
    {
        std::stringstream ss(line);

        std::string prefix;
        ss >> prefix;

        //-----------------------------------------------------------------------------------------------------------------//
        // Comment.
        //-----------------------------------------------------------------------------------------------------------------//
        if(prefix == "#") { continue; }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // New material.
        //-----------------------------------------------------------------------------------------------------------------//
        if(prefix == "newmtl")
        {
            m_materials.push_back(curr_material);
            m_material_name_to_index[curr_material.m_name] = m_materials.size() - 1;

            curr_material = Material();
            curr_material.m_name = parseName(ss);
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Diffuse colour.
        //-----------------------------------------------------------------------------------------------------------------//
        else if(prefix == "Kd")
        {
            curr_material.m_diffuse = parseColor(ss);
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Diffuse texture.
        //-----------------------------------------------------------------------------------------------------------------//
        else if(prefix == "map_Kd")
        {
            const std::string diffuse_texture_name = parseName(ss);

            if(m_diffuse_texture_name_to_index.find(diffuse_texture_name) == m_diffuse_texture_name_to_index.end())
            {
                Texture* new_texture = Texture::loadTextureJpgFile(file_folder, diffuse_texture_name);
                m_diffuse_textures.push_back(new_texture);
                m_diffuse_texture_name_to_index[diffuse_texture_name] = m_diffuse_textures.size() - 1;
            }
            curr_material.m_diffuse_texture = m_diffuse_textures[m_diffuse_texture_name_to_index[diffuse_texture_name]];
        }
        //-----------------------------------------------------------------------------------------------------------------//
    }
    
    m_materials.push_back(curr_material);
    m_material_name_to_index[curr_material.m_name] = m_materials.size() - 1;
}
