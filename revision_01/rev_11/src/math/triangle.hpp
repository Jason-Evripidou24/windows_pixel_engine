namespace Math
{
    struct Triangle
    {
        //-----------------------------------------------------------------------------------------------------------------//
        Vertex m_v0;
        Vertex m_v1;
        Vertex m_v2;
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        Triangle()
        {
            m_v0 = Vertex();
            m_v1 = Vertex();
            m_v2 = Vertex();
        }

        Triangle(const Vertex& v_0, const Vertex& v_1, const Vertex& v_2)
        {
            m_v0 = v_0;
            m_v1 = v_1;
            m_v2 = v_2;
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Visulisation.
        //-----------------------------------------------------------------------------------------------------------------//
        std::string toString(int min_num_width, int num_decimals) const
        {
            return
            (
                std::string("TRIANGLE INFO:") + std::string("\n") +
                m_v0.toString(min_num_width, num_decimals) + std::string("\n") +
                m_v1.toString(min_num_width, num_decimals) + std::string("\n") +
                m_v2.toString(min_num_width, num_decimals)
            );
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
