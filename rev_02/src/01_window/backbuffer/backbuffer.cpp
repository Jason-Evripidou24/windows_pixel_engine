// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Backbuffer::Backbuffer()
{
    m_bitmapinfo = {};

    m_color_buffer = nullptr;
    m_depth_buffer = nullptr;

    m_width = 0;
    m_height = 0;
    
    m_pitch = 0;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Backbuffer::~Backbuffer()
{
    this->freeBuffers();
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::freeBuffers()
{
    if(m_color_buffer != nullptr)
    {
        VirtualFree(m_color_buffer, 0, MEM_RELEASE);
        m_color_buffer = nullptr;
    }

    if(m_depth_buffer != nullptr)
    {
        VirtualFree(m_depth_buffer, 0, MEM_RELEASE);
        m_depth_buffer = nullptr;
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::resize(int width, int height)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if
    (
        (width <= 0)  ||
        (height <= 0) ||
        (
            (width == m_width)   &&
            (height == m_height)
        )
    )
    {
        return;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    this->freeBuffers();

    //---------------------------------------------------------------------------------------------------------------------//
    m_width  = width;
    m_height = height;
    m_pitch  = m_width * sizeof(Pixel);

    m_bitmapinfo = {};
    m_bitmapinfo.bmiHeader.biSize = sizeof(BITMAPINFOHEADER);
    m_bitmapinfo.bmiHeader.biWidth = m_width;
    m_bitmapinfo.bmiHeader.biHeight = -m_height; // top-down
    m_bitmapinfo.bmiHeader.biPlanes = 1;
    m_bitmapinfo.bmiHeader.biBitCount = 32;
    m_bitmapinfo.bmiHeader.biCompression = BI_RGB;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    size_t pixel_count = static_cast<size_t>(m_width) * static_cast<size_t>(m_height);

    size_t color_buffer_size = pixel_count * sizeof(Pixel);
    m_color_buffer = (Pixel*)VirtualAlloc
    (
        0,
        color_buffer_size,
        MEM_COMMIT | MEM_RESERVE,
        PAGE_READWRITE
    );

    size_t depth_buffer_size = pixel_count * sizeof(float);
    m_depth_buffer = (float*)VirtualAlloc
    (
        0,
        depth_buffer_size,
        MEM_COMMIT | MEM_RESERVE,
        PAGE_READWRITE
    );

    if( (m_color_buffer == nullptr) || (m_depth_buffer == nullptr) )
    {
        this->freeBuffers();
        m_width = 0;
        m_height = 0;
        m_pitch = 0;
        m_bitmapinfo = {};
    }
    //---------------------------------------------------------------------------------------------------------------------//
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::clear(Pixel color)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if
    (
        (m_color_buffer == nullptr) ||
        (m_depth_buffer == nullptr)
    )
    {
        return;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    int count = m_width * m_height;

    for(int i = 0; i < count; i++)
    {
        m_color_buffer[i] = color;
        m_depth_buffer[i] = -1.0f;
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::setPixel(float x, float y, float depth, Pixel color)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if
    (
        (m_color_buffer == nullptr) ||
        (m_depth_buffer == nullptr) ||
        (x < -1.0f)                 ||
        (x > 1.0f)                  ||
        (y < -1.0f)                 ||
        (y > 1.0f)                  ||
        (depth < -1.0f)             ||
        (depth > 1.0f)
    )
    {
        return;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    int buffers_x_coord = (x + 1.0f) * m_width * 0.5f;
    if(buffers_x_coord < 0)             { buffers_x_coord = 0; }
    else if(buffers_x_coord >= m_width) { buffers_x_coord = m_width - 1; }

    int buffers_y_coord = (-y + 1.0f) * m_height * 0.5f;
    if(buffers_y_coord < 0)              { buffers_y_coord = 0; }
    else if(buffers_y_coord >= m_height) { buffers_y_coord = m_height - 1; }

    int buffers_index = (buffers_y_coord * m_width) + buffers_x_coord;
    
    if(depth > m_depth_buffer[buffers_index])
    {
        m_color_buffer[buffers_index] = color;
        m_depth_buffer[buffers_index] = depth;
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::present(HDC dc, int window_width, int window_height)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if
    (
        (m_color_buffer == nullptr) ||
        (m_depth_buffer == nullptr)
    )
    {
        return;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    StretchDIBits
    (
        dc,
        0,
        0,
        window_width,
        window_height,
        0,
        0,
        m_width,
        m_height,
        m_color_buffer,
        &m_bitmapinfo,
        DIB_RGB_COLORS,
        SRCCOPY
    );
}
