// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawPixel(Backbuffer* backbuffer, float x, float y, float depth, const Pixel& color)
{
    backbuffer->setPixel(x, y, depth, color);
}
