// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::perspectiveDivideTriangle(Math::Triangle& triangle)
{
    for(int i = 0; i < 3; i++)
    {
        Math::Vec4_f& position = triangle.m_vertices[i].m_position;

        float w = position.m_data[3];

        position.m_data[0] /= w;
        position.m_data[1] /= w;
        position.m_data[2] /= w;
        position.m_data[3] = 1.0f;
    }
}
