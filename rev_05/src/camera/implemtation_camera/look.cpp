// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Camera::lookRight(const float offset)
{
    m_yaw_rads += offset;
    m_yaw_rads = std::fmod(m_yaw_rads, Math::TWO_PI_f);

    this->updateVectors();
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Camera::lookUp(const float offset)
{
    m_pitch_rads += offset;
    m_pitch_rads = std::clamp<float>(m_pitch_rads, m_CAMERA_MIN_PITCH_RADIANS_f, m_CAMERA_MAX_PITCH_RADIANS_f);

    this->updateVectors();
}
