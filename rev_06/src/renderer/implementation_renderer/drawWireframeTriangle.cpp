/*
-   Triangle has already been transformed to clip space.
*/
void Renderer::drawWireframeTriangle(Backbuffer& backbuffer, const Math::Triangle& triangle)
{
    this->drawLine(backbuffer, triangle.m_vertices[0], triangle.m_vertices[1]);
    this->drawLine(backbuffer, triangle.m_vertices[0], triangle.m_vertices[2]);
    this->drawLine(backbuffer, triangle.m_vertices[1], triangle.m_vertices[2]);
}
