// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Window::resize(int width, int height)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if
    (
        (width == m_width && height == m_height) ||
        (width <= 0) ||
        (height <= 0)
    ) 
    {
        return;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    m_width  = width;
    m_height = height;
}
