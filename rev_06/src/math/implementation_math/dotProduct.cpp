// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
float Math::dotProduct(const Vec4_f& vec_l, const Vec4_f& vec_r)
{
    float dot_product = 0.0f;

    for(int i = 0; i < 4; i++)
    {
        dot_product += vec_l.m_data[i] * vec_r.m_data[i];
    }

    return dot_product;
}
