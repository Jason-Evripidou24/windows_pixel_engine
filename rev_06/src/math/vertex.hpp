namespace Math
{
    struct Vertex
    {
        //-----------------------------------------------------------------------------------------------------------------//
        Math::Vec4_f m_position;
        uint32_t m_color;
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Visulisation.
        //-----------------------------------------------------------------------------------------------------------------//
        std::string toString(int min_num_width, int num_decimals) const
        {
            return
            (
                m_position.toStringRow(min_num_width, num_decimals) + std::string("\n") +
                std::to_string(m_color)
            );
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
