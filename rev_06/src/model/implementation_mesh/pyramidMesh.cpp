Mesh Mesh::pyramidMesh()
{
    Mesh pyramid_mesh;
    
    int vertex_count    = 5;
    int triangles_count = 6;

    //-----------------------------------------------------------------------------------------------------------------//
    // Vertices: Position and colour.
    //-----------------------------------------------------------------------------------------------------------------//
    float right_x = 0.5f;
    float left_x = -0.5f;

    float top_y = 0.5f;
    float bottom_y = -0.5f;

    float front_z = 0.5f;
    float back_z = -0.5f;

    pyramid_mesh.m_vertices = std::vector<Math::Vertex>(vertex_count);

    // Apex
    pyramid_mesh.m_vertices[0].m_position = Math::Vec4_f(0.0f, 0.5f, 0.0f, 1.0f);
    pyramid_mesh.m_vertices[0].m_color = 0xFFFF00FF; // Magenta

    // Base - Front Left
    pyramid_mesh.m_vertices[1].m_position = Math::Vec4_f(-0.5f, -0.5f, 0.5f, 1.0f);
    pyramid_mesh.m_vertices[1].m_color = 0xFFFFA500; // Orange

    // Base - Front Right
    pyramid_mesh.m_vertices[2].m_position = Math::Vec4_f(0.5f, -0.5f, 0.5f, 1.0f);
    pyramid_mesh.m_vertices[2].m_color = 0xFF87CEEB; // Sky Blue

    // Base - Back Right
    pyramid_mesh.m_vertices[3].m_position = Math::Vec4_f(0.5f, -0.5f, -0.5f, 1.0f);
    pyramid_mesh.m_vertices[3].m_color = 0xFF00FF00; // Green

    // Base - Back Left
    pyramid_mesh.m_vertices[4].m_position = Math::Vec4_f(-0.5f, -0.5f, -0.5f, 1.0f);
    pyramid_mesh.m_vertices[4].m_color = 0xFFFFFF00; // Yellow
    //-----------------------------------------------------------------------------------------------------------------//

    //-----------------------------------------------------------------------------------------------------------------//
    // Indicies: Each triplet represents a triangle face.
    //-----------------------------------------------------------------------------------------------------------------//
    pyramid_mesh.m_triangle_indices = std::vector<Math::Vec3_i>(triangles_count);

    pyramid_mesh.m_triangle_indices[0] = Math::Vec3_i(0, 1, 2);
    pyramid_mesh.m_triangle_indices[1] = Math::Vec3_i(0, 2, 3);
    pyramid_mesh.m_triangle_indices[2] = Math::Vec3_i(0, 3, 4);
    pyramid_mesh.m_triangle_indices[3] = Math::Vec3_i(0, 4, 1);

    pyramid_mesh.m_triangle_indices[4] = Math::Vec3_i(1, 4, 3);
    pyramid_mesh.m_triangle_indices[5] = Math::Vec3_i(1, 3, 2);
    //-----------------------------------------------------------------------------------------------------------------//

    return pyramid_mesh;
}
