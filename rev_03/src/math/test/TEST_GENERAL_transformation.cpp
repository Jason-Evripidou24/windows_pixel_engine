// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
// Test 03.
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
bool TEST_GENERAL_transformation_03()
{
    std::cout << "##### ##### ##### ##### TEST ##### ##### ##### #####" << std::endl;
    std::cout << "TEST_GENERAL_transformation_03: " << std::endl;

    //---------------------------------------------------------------------------------------------------------------------//
    // Object in local space
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f position(1.0f, 0.0f, -5.0f, 1.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // MODEL: translate by +5 on X
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f model = Math::translationMat4_f(5.0f, 0.0f, 0.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // VIEW: camera at +2 on X (inverse translation = -2)
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f view = Math::translationMat4_f(-2.0f, 0.0f, 0.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // PROJECTION: simple perspective
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f projection = Math::perspectiveMat4_f
    (
        3.1415926f / 2.0f,  // 90 degrees
        1.0f,               // aspect
        1.0f,               // near
        10.0f               // far
    );

    //---------------------------------------------------------------------------------------------------------------------//
    // STEP 1: Model
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f world = Math::multiply(model, position);
    world.m_data[3] = 1.0f;
    std::cout << "world:      " << world.toString() << std::endl;

    Math::Vec4_f expected_world(6.0f, 0.0f, 0.0f, 1.0f);

    if(world != expected_world)
    {
        std::cout << "FAIL: MODEL STAGE" << std::endl;
        std::cout << world.toString() << std::endl;
        return false;
    }

    //---------------------------------------------------------------------------------------------------------------------//
    // STEP 2: View
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f view_space = Math::multiply(view, world);
    view_space.m_data[3] = 1.0f;
    std::cout << "view_space: " << view_space.toString() << std::endl;

    Math::Vec4_f expected_view(4.0f, 0.0f, 0.0f, 1.0f);

    if(view_space != expected_view)
    {
        std::cout << "FAIL: VIEW STAGE" << std::endl;
        std::cout << view_space.toString() << std::endl;
        return false;
    }

    //---------------------------------------------------------------------------------------------------------------------//
    // STEP 3: Projection
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f clip = Math::multiply(projection, view_space);
    std::cout << "clip:       " << clip.toString() << std::endl;

    //---------------------------------------------------------------------------------------------------------------------//
    // Validation (projection is harder to exact-match)
    //---------------------------------------------------------------------------------------------------------------------//
    if(clip.m_data[3] == 0.0f)
    {
        std::cout << "FAIL: INVALID W in projection" << std::endl;
        return false;
    }

    float ndc_x = clip.m_data[0] / clip.m_data[3];
    float ndc_y = clip.m_data[1] / clip.m_data[3];
    float ndc_z = clip.m_data[2] / clip.m_data[3];

    std::cout << "Clip: " << clip.toString() << std::endl;
    std::cout << "NDC : (" << ndc_x << ", " << ndc_y << ", " << ndc_z << ")" << std::endl;

    //---------------------------------------------------------------------------------------------------------------------//
    // Basic sanity checks for projection
    //---------------------------------------------------------------------------------------------------------------------//
    if(ndc_x < -1.1f || ndc_x > 1.1f)
    {
        std::cout << "FAIL: X out of NDC range" << std::endl;
        return false;
    }

    if(ndc_y < -1.1f || ndc_y > 1.1f)
    {
        std::cout << "FAIL: Y out of NDC range" << std::endl;
        return false;
    }

    //---------------------------------------------------------------------------------------------------------------------//
    // PASS
    //---------------------------------------------------------------------------------------------------------------------//
    std::cout << "PASSED MVP TEST" << std::endl;
    return true;
}
