// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
bool TEST_GENERAL_transformation_02()
{
    std::cout << "##### ##### ##### ##### TEST ##### ##### ##### #####" << std::endl;
    std::cout << "TEST_GENERAL_transformation_02: " << std::endl;

    //---------------------------------------------------------------------------------------------------------------------//
    // Vertex (object space)
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f position(1.0f, 0.0f, 0.0f, 1.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // Model matrix: translate object by +5 on X
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f model = Math::translationMat4_f(5.0f, 0.0f, 0.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // View matrix: camera at +2 on X, no rotation
    // (equivalent to translating world by -2)
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f view = Math::translationMat4_f(-2.0f, 0.0f, 0.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // Expected result: (1 + 5 - 2, 0, 0)
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f expected(4.0f, 0.0f, 0.0f, 1.0f);

    //---------------------------------------------------------------------------------------------------------------------//
    // Apply transformations
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f result = Math::multiply(model, position);
    result = Math::multiply(view, result);

    result.m_data[3] = 1.0f; // test-only safety

    //---------------------------------------------------------------------------------------------------------------------//
    // Check if the transformed position is where it should be.
    //---------------------------------------------------------------------------------------------------------------------//
    if(result == expected)
    {
        std::cout << "PASSED :)" << std::endl;
        std::cout << "##### ##### ##### ##### #### ##### ##### ##### #####" << std::endl;
        return true;
    }
    std::cout << "FAILED!!! :(" << std::endl;
    std::cout << "##### ##### ##### ##### #### ##### ##### ##### #####" << std::endl;
    return false;
    //---------------------------------------------------------------------------------------------------------------------//
}
