// Test 01.
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
bool TEST_GENERAL_transformation_01()
{
    std::cout << "##### ##### ##### ##### TEST ##### ##### ##### #####" << std::endl;
    std::cout << "TEST_GENERAL_transformation_01: " << std::endl;

    //---------------------------------------------------------------------------------------------------------------------//
    // Vertex (right handed coord system).
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f position(-1.0f, 1.0f, -1.0f, 1.0f);
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    /*
    -   Translate, Scale and Rotate matricies:
        -   Rotate 90 degrees around y axis.
        -   Scale 2x in the x axis.
        -   Translate 2 in the x axis.
    */
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Mat4_f translate = Math::translationMat4_f(2.0f, 0.0f, 0.0f);
    Math::Mat4_f scale = Math::scaleMat4_f(2.0f, 1.0f, 1.0f);
    Math::Mat4_f rotate = Math::rotationMat4_f(0.0f, 1.0f, 0.0f, 3.1415926f / 2.0f);

    Math::Mat4_f model = Math::multiply(Math::multiply(translate, rotate), scale);
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    /*
    -   Transformations should be in the order: Scale, Rotation, Translation:
        -   Scale     (-1.0f, 1.0f, -1.0f) 2x in the x axis will give:         (-2.0f, 1.0f, -1.0f)
        -   Rotate    (-2.0f, 1.0f, -1.0f) 90 degrees around y axis will give: (-1.0f, 1.0f,  2.0f)
        -   Translate (-1.0f, 1.0f, 2.0f)  2 units in the x axis will give:    ( 1.0f, 1.0f,  2.0f)
    */
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f position_expected(1.0f, 1.0f, 2.0f, 1.0f);
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Apply transformation to the vertices.
    //---------------------------------------------------------------------------------------------------------------------//
    Math::Vec4_f position_transformed = Math::multiply(model, position);
    position_transformed.m_data[3] = 1.0f; // Manually change the w component for this test.
    //std::cout << position_transformed.toString() << std::endl;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Check if the transformed positions are where they should be.
    //---------------------------------------------------------------------------------------------------------------------//
    if(position_transformed == position_expected)
    {
        std::cout << "PASSED :)" << std::endl;
        std::cout << "##### ##### ##### ##### #### ##### ##### ##### #####" << std::endl;
        return true;
    }
    std::cout << "FAILED!!! :(" << std::endl;
    std::cout << "##### ##### ##### ##### #### ##### ##### ##### #####" << std::endl;
    return false;
    //---------------------------------------------------------------------------------------------------------------------//
}
