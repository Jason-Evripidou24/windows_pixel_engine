// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Math::Vec2_f Math::scalarMultiply(const Vec2_f& vec, const float scalar)
{
    Vec2_f new_vec;

    new_vec.m_data[0] = vec.m_data[0] * scalar;
    new_vec.m_data[1] = vec.m_data[1] * scalar;

    return new_vec;
}
