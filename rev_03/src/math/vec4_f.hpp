/*
-   Note when using Vec4_f in pixel engine:
    -   Vec4_f consists of {x, y, z, w}. The w is the "homogeneous coordinate", and it exists to make 3D transformations
        work correctly."
    -   w controls how the vector behaves under transformations.
    -   When w = 1.0f i.e. {x, y, z, 1.0f}, the vector represents a real point in space. Translation, Scale and Rotation
        matricies all affect it.
    -   When w = 0.0f i.e. {x, y, z, 0.0f}, the vector represents direction. Translation does NOT affect it, only Scale and
        Rotation matricies affect it.
*/
namespace Math
{
    struct Vec4_f
    {
        //-----------------------------------------------------------------------------------------------------------------//
        float m_data[4];
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        Vec4_f() { m_data[0] = 0.0f; m_data[1] = 0.0f; m_data[2] = 0.0f; m_data[3] = 0.0f; }
        Vec4_f(float x, float y, float z, float w) { m_data[0] = x; m_data[1] = y; m_data[2] = z; m_data[3] = w; }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        bool operator==(const Vec4_f& other) const
        {
            const float eps = 0.0001f;

            for(int i = 0; i < 4; i++)
            {
                float this_val = m_data[i];
                float other_val = other.m_data[i];

                float smaller_val = this_val;
                if(other_val < smaller_val) { smaller_val = other_val; }

                float larger_val = this_val;
                if(other_val > larger_val) { larger_val = other_val; }

                if( (larger_val - smaller_val) > eps)
                {
                    return false;
                }
            }
            return true;
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
