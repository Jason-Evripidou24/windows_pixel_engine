#ifndef TIMER_HPP
#define TIMER_HPP
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
//-------------------------------------------------------------------------------------------------------------------------//
// Standard library.
//-------------------------------------------------------------------------------------------------------------------------//
//-------------------------------------------------------------------------------------------------------------------------//

//-------------------------------------------------------------------------------------------------------------------------//
// Third party.
//-------------------------------------------------------------------------------------------------------------------------//
#include <windows.h>
//-------------------------------------------------------------------------------------------------------------------------//

//-------------------------------------------------------------------------------------------------------------------------//
// Internal.
//-------------------------------------------------------------------------------------------------------------------------//
//-------------------------------------------------------------------------------------------------------------------------//
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
struct Timer
{
    //---------------------------------------------------------------------------------------------------------------------//
    // Data.
    //---------------------------------------------------------------------------------------------------------------------//
    LARGE_INTEGER freq;
    LARGE_INTEGER last;

    float deltaTime = 0.0f;

    // FPS tracking
    float fpsTimer = 0.0f;
    int frameCount = 0;
    int fps = 0;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Functions.
    //---------------------------------------------------------------------------------------------------------------------//
    void init()
    {
        QueryPerformanceFrequency(&freq);
        QueryPerformanceCounter(&last);
    }

    void tick()
    {
        LARGE_INTEGER now;
        QueryPerformanceCounter(&now);

        deltaTime = (float)(now.QuadPart - last.QuadPart) / (float)freq.QuadPart;

        last = now;

        //-----------------------------------------------------------------------------------------------------------------//
        // FPS logic
        //-----------------------------------------------------------------------------------------------------------------//
        frameCount++;
        fpsTimer += deltaTime;

        if (fpsTimer >= 1.0f)
        {
            fps = frameCount;
            frameCount = 0;
            fpsTimer = 0.0f;
        }
        //-----------------------------------------------------------------------------------------------------------------//
    }
    //---------------------------------------------------------------------------------------------------------------------//
};
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
#endif
