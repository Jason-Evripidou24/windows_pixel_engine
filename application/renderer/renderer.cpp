// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::init(Backbuffer* backbuffer)
{
    m_backbuffer = backbuffer;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::clear(const Pixel& color)
{
    m_backbuffer->clear(color);
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawPixel(int x, int y, const Pixel& color)
{
    m_backbuffer->setPixel(x, y, color);
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawLine(int x0, int y0, int x1, int y1, const Pixel& color)
{
    int dx = std::abs(x1 - x0);
    int dy = std::abs(y1 - y0);

    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;

    int err = dx - dy;

    while (true)
    {
        this->drawPixel(x0, y0, color);

        if (x0 == x1 && y0 == y1)
            break;

        int e2 = 2 * err;

        if (e2 > -dy)
        {
            err -= dy;
            x0 += sx;
        }

        if (e2 < dx)
        {
            err += dx;
            y0 += sy;
        }
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawRect(int x, int y, int w, int h, const Pixel& color)
{
    this->drawLine(x,     y,     x + w, y,     color); // top
    this->drawLine(x + w, y,     x + w, y + h, color); // right
    this->drawLine(x + w, y + h, x,     y + h, color); // bottom
    this->drawLine(x,     y + h, x,     y,     color); // left
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawFilledRect(int x, int y, int w, int h, const Pixel& color)
{
    for(int j = 0; j < h; j++)
    {
        int py = y + j;

        for(int i = 0; i < w; i++)
        {
            this->drawPixel(x + i, py, color);
        }
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawCircle(int cx, int cy, int r, const Pixel& color)
{
    int x = r;
    int y = 0;
    int err = 0;

    while (x >= y)
    {
        // 8-way symmetry
        this->drawPixel(cx + x, cy + y, color);
        this->drawPixel(cx + y, cy + x, color);
        this->drawPixel(cx - y, cy + x, color);
        this->drawPixel(cx - x, cy + y, color);
        this->drawPixel(cx - x, cy - y, color);
        this->drawPixel(cx - y, cy - x, color);
        this->drawPixel(cx + y, cy - x, color);
        this->drawPixel(cx + x, cy - y, color);

        y++;

        if(err <= 0)
        {
            err += 2 * y + 1;
        }
        else
        {
            x--;
            err += 2 * (y - x) + 1;
        }
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Renderer::drawFilledCircle(int cx, int cy, int r, const Pixel& color)
{
    for (int y = -r; y <= r; y++)
    {
        int dx = (int)sqrtf((float)(r * r - y * y));

        int xStart = cx - dx;
        int xEnd   = cx + dx;
        int py     = cy + y;

        for (int x = xStart; x <= xEnd; x++)
        {
            drawPixel(x, py, color);
        }
    }
}
