struct Mat4_f
{
    //---------------------------------------------------------------------------------------------------------------------//
    float m_data[16];
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    std::string toString()
    {
        return std::string
        (
            std::to_string(m_data[0]) + " " + std::to_string(m_data[1]) + " " +
            std::to_string(m_data[2]) + " " + std::to_string(m_data[3]) + "\n" +
            
            std::to_string(m_data[4]) + " " + std::to_string(m_data[5]) + " " +
            std::to_string(m_data[6]) + " " + std::to_string(m_data[7]) + "\n" +

            std::to_string(m_data[8]) + " " + std::to_string(m_data[9]) + " " +
            std::to_string(m_data[10]) + " " + std::to_string(m_data[11]) + "\n" +

            std::to_string(m_data[12]) + " " + std::to_string(m_data[13]) + " " +
            std::to_string(m_data[14]) + " " + std::to_string(m_data[15])
        );
    }
    //---------------------------------------------------------------------------------------------------------------------//
};
