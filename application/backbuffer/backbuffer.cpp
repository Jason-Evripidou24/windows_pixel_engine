// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Backbuffer::Backbuffer()
{
    m_memory = nullptr;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Backbuffer::~Backbuffer()
{
    this->free();
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::free()
{
    if(m_memory == nullptr) { return; }

    VirtualFree(m_memory, 0, MEM_RELEASE);
    m_memory = nullptr;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::resize(int width, int height)
{
    this->free();

    m_width  = width;
    m_height = height;
    m_pitch  = width * sizeof(Pixel);

    m_bitmapinfo = {};
    m_bitmapinfo.bmiHeader.biSize = sizeof(BITMAPINFOHEADER);
    m_bitmapinfo.bmiHeader.biWidth = width;
    m_bitmapinfo.bmiHeader.biHeight = -height; // top-down
    m_bitmapinfo.bmiHeader.biPlanes = 1;
    m_bitmapinfo.bmiHeader.biBitCount = 32;
    m_bitmapinfo.bmiHeader.biCompression = BI_RGB;

    size_t size = width * height * sizeof(Pixel);

    m_memory = (Pixel*)VirtualAlloc
    (
        0,
        size,
        MEM_COMMIT | MEM_RESERVE,
        PAGE_READWRITE
    );
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::clear(Pixel color)
{
    int count = m_width * m_height;

    for(int i = 0; i < count; i++)
    {
        m_memory[i] = color;
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::setPixel(int x, int y, Pixel color)
{
    if( (x < 0) || (x >= m_width) || (y < 0) || (y >= m_height) ) { return; }

    m_memory[(y * m_width) + x] = color;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Pixel& Backbuffer::getPixel(int x, int y)
{
    return m_memory[y * m_width + x];
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::present(HDC dc, int window_width, int window_height)
{
    StretchDIBits
    (
        dc,
        0,
        0,
        window_width,
        window_height,
        0,
        0,
        m_width,
        m_height,
        m_memory,
        &m_bitmapinfo,
        DIB_RGB_COLORS,
        SRCCOPY
    );
}
