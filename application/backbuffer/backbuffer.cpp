// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Backbuffer::Backbuffer()
{
    m_color_buffer = nullptr;
    m_depth_buffer = nullptr;
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Backbuffer::~Backbuffer()
{
    this->free();
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::free()
{
    if(m_color_buffer != nullptr)
    {
        VirtualFree(m_color_buffer, 0, MEM_RELEASE);
        m_color_buffer = nullptr;
    }

    if(m_depth_buffer != nullptr)
    {
        VirtualFree(m_depth_buffer, 0, MEM_RELEASE);
        m_depth_buffer = nullptr;
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::resize(int width, int height)
{
    this->free();

    m_width  = width;
    m_height = height;
    m_pitch  = m_width * sizeof(Pixel);

    m_bitmapinfo = {};
    m_bitmapinfo.bmiHeader.biSize = sizeof(BITMAPINFOHEADER);
    m_bitmapinfo.bmiHeader.biWidth = m_width;
    m_bitmapinfo.bmiHeader.biHeight = -m_height; // top-down
    m_bitmapinfo.bmiHeader.biPlanes = 1;
    m_bitmapinfo.bmiHeader.biBitCount = 32;
    m_bitmapinfo.bmiHeader.biCompression = BI_RGB;

    size_t color_buffer_size = m_width * m_height * sizeof(Pixel);
    m_color_buffer = (Pixel*)VirtualAlloc
    (
        0,
        color_buffer_size,
        MEM_COMMIT | MEM_RESERVE,
        PAGE_READWRITE
    );

    size_t depth_buffer_size = m_width * m_height * sizeof(float);
    m_depth_buffer = (float*)VirtualAlloc
    (
        0,
        depth_buffer_size,
        MEM_COMMIT | MEM_RESERVE,
        PAGE_READWRITE
    );
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::clear(Pixel color)
{
    int count = m_width * m_height;

    for(int i = 0; i < count; i++)
    {
        m_color_buffer[i] = color;
        m_depth_buffer[i] = 1.0f; // far plane.
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::setPixel(int x, int y, float depth, Pixel color)
{
    if( (x < 0) || (x >= m_width) || (y < 0) || (y >= m_height) ) { return; }

    int index = (y * m_width) + x;
    
    if(depth < m_depth_buffer[index])
    {
        m_color_buffer[(y * m_width) + x] = color;
        m_depth_buffer[(y * m_width) + x] = depth;
    }
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
Pixel& Backbuffer::getPixel(int x, int y)
{
    return m_color_buffer[y * m_width + x];
}
// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //


// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::present(HDC dc, int window_width, int window_height)
{
    StretchDIBits
    (
        dc,
        0,
        0,
        window_width,
        window_height,
        0,
        0,
        m_width,
        m_height,
        m_color_buffer,
        &m_bitmapinfo,
        DIB_RGB_COLORS,
        SRCCOPY
    );
}
