/*
-   Renderer pipeline:
    1.  Local/Object Space (Vec3, Vec4 with 1.0f as the homogenious coordinate)
    |   Model Matrix
    2.  World Space (Vec4)
    |   View Matrix
    3.  View Space
    |   Projection Matrix
    4.  Clip Space (Vec4)
    |   Homogeneous Clipping
    |   Perspective Divide
    5.  Normalized Device Coordinates (NDC)
    |   Viewport Transform
    6.  Screen Space
    |   Rasterization, Depth Test, Framebuffer
*/
struct Renderer
{
    //---------------------------------------------------------------------------------------------------------------------//
    // Functions.
    //---------------------------------------------------------------------------------------------------------------------//
    void drawModel
    (
        Backbuffer& backbuffer,
        const Model& model,
        const Math::Mat4_f& projection_view_matrix,
        const bool draw_filled
    );
    
    void drawTriangle(Backbuffer& backbuffer, const Math::Triangle& triangle, const Material& material, const bool draw_filled);
    void fillTriangle(Backbuffer& backbuffer, const Math::Triangle& triangle, const Material& material);

    void drawLine(Backbuffer& backbuffer, const Math::Vertex& v_0, const Math::Vertex& v_1, const Material& material);
    void drawPixel(Backbuffer& backbuffer, const Math::Vertex& vertex, const Material& material);
    //---------------------------------------------------------------------------------------------------------------------//
};
