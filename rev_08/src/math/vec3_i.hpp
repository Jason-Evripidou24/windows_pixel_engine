namespace Math
{
    struct Vec3_i
    {
        //-----------------------------------------------------------------------------------------------------------------//
        int m_data[3];
        //-----------------------------------------------------------------------------------------------------------------//
        
        //-----------------------------------------------------------------------------------------------------------------//
        Vec3_i() { m_data[0] = 0; m_data[1] = 0; m_data[2] = 0; }
        Vec3_i(int x, int y, int z) { m_data[0] = x; m_data[1] = y; m_data[2] = z; }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Comparison operator overloads.
        //-----------------------------------------------------------------------------------------------------------------//
        bool operator==(const Vec3_i& other) const
        {
            for(int i = 0; i < 3; i++)
            {
                if(m_data[i] != other.m_data[i])
                {
                    return false;
                }
            }
            return true;
        }

        bool operator!=(const Vec3_i& other) const
        {
            for(int i = 0; i < 3; i++)
            {
                if(m_data[i] == other.m_data[i])
                {
                    return false;
                }
            }
            return true;
        }
        //-----------------------------------------------------------------------------------------------------------------//

        //-----------------------------------------------------------------------------------------------------------------//
        // Arithmetic operator overloads.
        //-----------------------------------------------------------------------------------------------------------------//
        Vec3_i operator+(const Vec3_i& rhs) const
        {
            return Vec3_i
            (
                m_data[0] + rhs.m_data[0],
                m_data[1] + rhs.m_data[1],
                m_data[2] + rhs.m_data[2]
            );
        }

        Vec3_i operator-(const Vec3_i& rhs) const
        {
            return Vec3_i
            (
                m_data[0] - rhs.m_data[0],
                m_data[1] - rhs.m_data[1],
                m_data[2] - rhs.m_data[2]
            );
        }

        Vec3_i operator*(const int scalar) const
        {
            return Vec3_i
            (
                m_data[0] * scalar,
                m_data[1] * scalar,
                m_data[2] * scalar
            );
        }

        Vec3_i operator/(const int scalar) const
        {
            return Vec3_i
            (
                m_data[0] / scalar,
                m_data[1] / scalar,
                m_data[2] / scalar
            );
        }
        //-----------------------------------------------------------------------------------------------------------------//
    };
};
