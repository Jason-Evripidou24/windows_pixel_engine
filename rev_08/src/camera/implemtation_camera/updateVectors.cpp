void Camera::updateVectors()
{
    m_front = Math::normalise
    (
        Math::Vec3_f
        (
            cosf(m_pitch_rads) * sinf(m_yaw_rads),
            sinf(m_pitch_rads),
            cosf(m_pitch_rads) * cosf(m_yaw_rads)
        )
    );

    m_right = Math::normalise
    (
        Math::crossProduct
        (
            m_front,
            m_world_up
        )
    );

    m_up = Math::normalise
    (
        Math::crossProduct
        (
            m_right,
            m_front
        )
    );
}
