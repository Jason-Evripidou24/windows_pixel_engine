struct RenderTriangle
{
    Math::Triangle m_triangle;
    const Material* m_material;

    //---------------------------------------------------------------------------------------------------------------------//
    RenderTriangle()
    {
        m_triangle = Math::Triangle();
        m_material = nullptr;
    }

    RenderTriangle(const Math::Triangle& triangle, const Material* material)
    {
        m_triangle = triangle;
        m_material = material;
    }
    //---------------------------------------------------------------------------------------------------------------------//
};
