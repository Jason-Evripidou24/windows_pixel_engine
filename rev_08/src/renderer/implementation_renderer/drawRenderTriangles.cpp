void Renderer::drawRenderTriangles
(
    Backbuffer& backbuffer,
    const std::vector<RenderTriangle>& render_triangles,
    const bool draw_filled
)
{
    for(size_t i = 0; i < render_triangles.size(); i++)
    {
        this->drawTriangle(backbuffer, render_triangles[i].m_triangle, *(render_triangles[i].m_material), draw_filled);
    }
}
