// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
struct Backbuffer
{
    //---------------------------------------------------------------------------------------------------------------------//
    // Data.
    //---------------------------------------------------------------------------------------------------------------------//
    int m_width;
    int m_height;
    
    int m_pitch; // bytes per row.

    BITMAPINFO m_bitmapinfo;

    uint32_t* m_color_buffer; // Stored as B-G-R-A.
    float* m_depth_buffer;
    //---------------------------------------------------------------------------------------------------------------------//

    //---------------------------------------------------------------------------------------------------------------------//
    // Functions.
    //---------------------------------------------------------------------------------------------------------------------//
    Backbuffer();
    ~Backbuffer();

    void freeBuffers();
    void resize(int width, int height);

    void clear(uint32_t color);
    void setPixel(int x, int y, float z, uint32_t color);

    void present(HDC dc, int window_width, int window_height);
    //---------------------------------------------------------------------------------------------------------------------//
};
