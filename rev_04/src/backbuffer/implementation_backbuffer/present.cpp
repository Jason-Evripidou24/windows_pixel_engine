// ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### ##### //
void Backbuffer::present(HDC dc, int window_width, int window_height)
{
    //---------------------------------------------------------------------------------------------------------------------//
    if
    (
        (m_color_buffer == nullptr) ||
        (m_depth_buffer == nullptr)
    )
    {
        return;
    }
    //---------------------------------------------------------------------------------------------------------------------//

    StretchDIBits
    (
        dc,
        0,
        0,
        window_width,
        window_height,
        0,
        0,
        m_width,
        m_height,
        m_color_buffer,
        &m_bitmapinfo,
        DIB_RGB_COLORS,
        SRCCOPY
    );
}
